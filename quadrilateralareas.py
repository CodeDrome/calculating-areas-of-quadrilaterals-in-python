
def square(side):

    return side ** 2


def rectangle(width, height):

    return width * height


def parallelogram(side, height):

    return side * height


def trapezium(height, width_1, width_2):

    return 0.5 * height * ( width_1 + width_2 )


def rhombus(height, width):

    return 0.5 * height * width


def kite(height, width):

    return 0.5 * height * width
